function stripContactInfo(text) {
  if (!text) return '';
  // Remove phone numbers, emails, names, zip codes from diagnosis text
  return text
    .replace(/\b[\w.+-]+@[\w-]+\.[\w.-]+\b/g, '[hidden]')           // emails
    .replace(/\b\d{3}[-.]?\d{3}[-.]?\d{4}\b/g, '[hidden]')          // phone numbers
    .replace(/\b\d{5}(-\d{4})?\b/g, '[area]')                        // zip codes
    .replace(/User is \w+/gi, 'The homeowner')                        // "User is Hugh" -> "The homeowner"
    .replace(/phone\s+[\d-]+/gi, 'phone [hidden]')                    // "phone 253-..." 
    .replace(/email\s+[\w.@]+/gi, 'email [hidden]');                  // "email xxx@..."
}

function truncateDiagnosis(text, maxLen) {
  if (!text || text.length <= maxLen) return text || '';
  return text.slice(0, maxLen).replace(/\s+\S*$/, '') + '...';
}

